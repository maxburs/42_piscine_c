void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	print_string(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}

int		main(int argc, char **argv)
{
	int i;

	i = 1;
	while (i < argc)
	{
		print_string(argv[i]);
		i++;
	}
	return (0);
}
