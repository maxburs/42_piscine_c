int		ft_is_prime(int nb)
{
	int counter;

	if (nb < 2)
	{
		return (0);
	}
	counter = 2;
	while (counter <= 46340)
	{
		if (nb % counter == 0)
		{
			return (0);
		}
		counter++;
	}
	return (1);
}
